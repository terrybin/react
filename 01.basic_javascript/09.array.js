// 배열 선언 방법
let arr = new Array();
let arr2 = [];
console.log(arr2);

let arr3 = [1, "2", true, null, undefined, []];
console.log(arr3); // [1, '2', true, null, undefined, Array(0)]


// 배열 접근법
let array = [1, 2, 3, 4, 5];
console.log(array[0]); // 1

array.push(6);
console.log(array); // [1, 2, 3, 4, 5, 6]


// 배열 길이
console.log(array.length); // 6